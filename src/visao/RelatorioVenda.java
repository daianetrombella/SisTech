    private void atualizarTabela() {
        try {
            vendas=  vendaDao.buscarPelaDescricao(jtf_pesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
            for (int i = 0; i < vendas.size(); i++) {
                Venda venda = vendas.get(i);
                model.addRow(new Object[]{venda.getId(), venda.getErp(), venda.getScm(),venda.getCrm(),venda.getSig(), venda.getAcomp(), venda.getTrein(), venda.getSup(), venda.getValor(), venda.getContaId()});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }
