    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int linha = tabela.getSelectedRow();
        if(linha < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma venda", "Atenção",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        vendaSelecionada = vendas.get(linha);
        int cod = vendas.get(linha).getId();
        try {
            vendaDao.remover(cod);
            JOptionPane.showMessageDialog(null, "Conta excluída");
            atualizarTabela();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        atualizarTabela();
    }//GEN-LAST:event_btn_excluirActionPerformed

