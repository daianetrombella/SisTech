    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int linha = tabela.getSelectedRow();
        if(linha < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma pessoa", "Atenção",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        contaSelecionada = contasR.get(linha);
        int cod = contasR.get(linha).getCod();
        try {
            contaDao.removerP(cod);
            JOptionPane.showMessageDialog(null, "Conta excluída");
            atualizarTabela();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        atualizarTabela();
    }//GEN-LAST:event_btn_excluirActionPerformed

