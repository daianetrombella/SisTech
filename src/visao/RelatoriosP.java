    private void atualizarTabela() {
        try {
            contasR = contaDao.buscarPelaDescricaoP(jtf_pesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
            for (int i = 0; i < contasR.size(); i++) {
                Conta conta = contasR.get(i);
                model.addRow(new Object[]{conta.getCod(), conta.getStatus(), conta.getNome(),conta.getNascimento(),conta.getTipo(), conta.getCnpj(), conta.getCelular(), conta.getNumeroNota(), conta.getValorNota(), conta.getDataNota()});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }
