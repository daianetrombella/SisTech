    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        con = true;
        valor = 0;
        //TIPOS DE SISTEMAS
        int t = 0;
        if(check_erp.isSelected()){
            valor = valor + 8000;
            t = t + 1;
        }
        if(check_crm.isSelected()){
            valor = valor + 7000;
            t = t + 1;
        }
        if(check_scm.isSelected()){
            valor = valor + 10000;
            t = t + 1;
        }
        if(check_sig.isSelected()){
            valor = valor + 9000;
            t = t + 1;
        }
        
        //SUPORTE
        if(jrb_supInt.isSelected()){
            if(t > 1){
                float v1 = t * 170;
                valor = valor + v1;
            }else if(t == 1){
                valor = valor + 170; 
            }else{
                //
            }
           
        }
        if(jrb_supPar.isSelected()){
            if(t > 1){
                float v1 = t * 100;
                valor = valor + v1;
            }else if(t == 1){
                valor = valor + 100; 
            }else{
                //
            }
        }
        if(jrb_semSup.isSelected()){
            valor = valor + 0;
        }
        
        //ACOMPANHAMENTO
        if(jrb_acomp.isSelected()){
            if(t > 1){
                float v1 = t * 1000;
                valor = valor + v1;
            }else if(t == 1){
                valor = valor + 1000; 
            }else{
                //
            }
            //valor = valor + 1000;
        }
        if(jrb_semAcomp.isSelected()){
            valor = valor + 0;
        }
        
        //TREINAMENTO
        if(jrb_trein.isSelected()){
            if(t > 1){
                float v1 = t * 200;
                valor = valor + v1;
            }else if(t == 1){
                valor = valor + 200; 
            }else{
                //
            }
            //valor = valor + 200;
        }
        if(jrb_semTrein.isSelected()){
            valor = valor + 0;
        }
       
        
        String valorCon = Float.toString(valor);
        jtf_valorTotal.setText(valorCon);
    }//GEN-LAST:event_btn_calcularActionPerformed

