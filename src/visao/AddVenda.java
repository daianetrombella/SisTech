    private void jrb_fecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jrb_fecharActionPerformed
        if(con == true && jtf_valorTotal.getText() != ""){
            String erp = "false";
            String crm = "false";
            String scm = "false";
            String sig = "false";
            String sup = null;
            String acomp = "false";
            String trein = "false";
            String valorTotal;
           // VendaDao vendaDao = null;
            //TIPOS DE SISTEMAS
            if(check_erp.isSelected()){
                erp = "true";
            }
            if(check_crm.isSelected()){
                crm = "true";
            }
            if(check_scm.isSelected()){
                scm = "true";
            }
            if(check_sig.isSelected()){
                sig = "true";
            }
            
            //SUPORTE
            if(jrb_supInt.isSelected()){
                sup = "Integral";
            }else if(jrb_supPar.isSelected()){
                sup = "Parcial";
            }else if(jrb_semSup.isSelected()){
                sup = "Sem suporte";
            }
            
            //ACOMPANHAMENTO
            if(jrb_acomp.isSelected()){
                acomp = "true";
            }else if(jrb_semAcomp.isSelected()){
                acomp = "false";
            }
            
            //TREINAMENTO
            if(jrb_trein.isSelected()){
                trein = "true";
            }else if(jrb_semTrein.isSelected()){
                trein = "false";
            }
            
            valorTotal = jtf_valorTotal.getText();
        
            if(alt == false){
                venda.setErp(erp);
                venda.setScm(scm);
                venda.setCrm(crm);
                venda.setSig(sig);
                venda.setAcomp(acomp);
                venda.setTrein(trein);
                venda.setSup(sup);
                venda.setValor(valorTotal);
                venda.setContaId("1");
     
                AddReceber rec = new AddReceber(usuario,venda);
                rec.setExtendedState(rec.getExtendedState()|JFrame.MAXIMIZED_BOTH);
                this.dispose();
                rec.setVisible(true);
            }else if(alt == true){
                venda.setErp(erp);
                venda.setScm(scm);
                venda.setCrm(crm);
                venda.setSig(sig);
                venda.setAcomp(acomp);
                venda.setTrein(trein);
                venda.setSup(sup);
                venda.setValor(valorTotal);
                venda.setContaId(id);
            
                try{
                    vendaDao.alterar(venda); 
                }catch (SQLException ex){
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Erro ao inserir no bd", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            
                AddReceber rec = new AddReceber(usuario,venda, id);
                rec.setExtendedState(rec.getExtendedState()|JFrame.MAXIMIZED_BOTH);
                this.dispose();
                rec.setVisible(true);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Calcule o valor total primeiro.", "Erro", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jrb_fecharActionPerformed

